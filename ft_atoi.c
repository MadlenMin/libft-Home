#include "libft.h"

int ft_atoi(const char *str)
{
	int i;
	int sign;
	long result;
	
	sign = 1;
	result = 0;
	i = 0;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		if (result > 2147483647 && sign == 1)
			return (2147483647);
		if (result > 2147483648 && sign == -1)
			return (-2147483648);
		i++;
	}
	return (sign * (int)result);
}
